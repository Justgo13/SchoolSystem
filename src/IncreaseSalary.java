/**
 * An interface that is used to increase the salary of school principal and teachers
 *
 * @author Jason Gao
 * @version v1.00 Dec 18,2019
 */
public interface IncreaseSalary
{
    /**
     * Increase the salary of the staff member by the value indicated by salaryIncrease
     *
     * @param salaryIncrease An int representing the amount to increase the salary by
     */
    void increaseSalary(int salaryIncrease);
}
